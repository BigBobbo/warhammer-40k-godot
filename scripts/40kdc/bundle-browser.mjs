#!/usr/bin/env node
// Build the browser artifacts for the army builder web app:
//
//  1. server/public/js/vendor/40kdc-data.js — the @alpaca-software/40kdc-data
//     package (dataset + roster/loadout/import engine) bundled as a single
//     minified ES module, plus the package's RAW_DATA (the raw authored
//     collections, needed for faction-scoped weapon resolution — the public
//     Dataset API dedups shared ids first-wins).
//
//  2. server/public/js/lib/canon.mjs — the game-name canonicalization table
//     extracted from the game's .gd dispatch tables (exact-name ability /
//     enhancement dispatch), so the browser emits the game's spellings.
//
// Usage: cd scripts/40kdc && npm install && node bundle-browser.mjs
//
// Both outputs are committed; rerun after bumping the package version or when
// the game's ability dispatch tables change.

import { writeFileSync, readFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath, pathToFileURL } from 'url';
import { createRequire } from 'module';
import esbuild from 'esbuild';

const here = dirname(fileURLToPath(import.meta.url));
const require = createRequire(import.meta.url);

const pkgEntry = require.resolve('@alpaca-software/40kdc-data');
const pkgJson = JSON.parse(readFileSync(
  join(dirname(pkgEntry), '..', 'package.json'), 'utf8'));
const rawBundlePath = join(dirname(pkgEntry), 'data', 'bundle.generated.js');

const outVendor = join(here, '..', '..', 'server', 'public', 'js', 'vendor', '40kdc-data.mjs');
const outCanon = join(here, '..', '..', 'server', 'public', 'js', 'lib', 'canon.mjs');

// ---------------------------------------------------------------------------
// 1. Vendor bundle
const banner = `/*!
 * @alpaca-software/40kdc-data v${pkgJson.version} — bundled for the browser.
 * Generated by scripts/40kdc/bundle-browser.mjs — do not edit by hand.
 *
 * Code: MIT + attribution (Alpaca Software). Embedded enrichment data:
 * CC BY 4.0 — Alpaca Software and the 40kdc community contributors,
 * https://github.com/wn-mitch/40kdc-data
 * Public deployments must display "Powered by 40kdc-data" with a link to
 * https://40kdc.alpacasoft.dev (see the package README).
 */`;

const entry = `
export * from '@alpaca-software/40kdc-data';
export { RAW_DATA } from ${JSON.stringify(rawBundlePath)};
export const DATASET_VERSION = ${JSON.stringify(pkgJson.version)};
`;

const result = await esbuild.build({
  stdin: {
    contents: entry,
    resolveDir: here,
    sourcefile: 'vendor-entry.mjs',
  },
  bundle: true,
  format: 'esm',
  platform: 'browser',
  target: 'es2020',
  minify: true,
  sourcemap: false,
  banner: { js: banner },
  outfile: outVendor,
  logLevel: 'warning',
  metafile: true,
});
const vendorBytes = Object.values(result.metafile.outputs)[0]?.bytes ?? 0;
console.log(`WROTE ${outVendor} (${(vendorBytes / 1024 / 1024).toFixed(2)} MB, v${pkgJson.version})`);

// ---------------------------------------------------------------------------
// 2. Game-name canon table (same extraction generate-armies.mjs uses).
const { buildCanonEntries } = await import(pathToFileURL(join(here, 'lib.mjs')).href);
const entries = buildCanonEntries();
const canonSrc = `// Game-name canonicalization table — names the game engine dispatches on
// (UnitAbilityManager / FactionAbilityManager / ArmyListManager tables).
// Generated by scripts/40kdc/bundle-browser.mjs — do not edit by hand;
// rerun the script when the game's .gd dispatch tables change.
export const GAME_NAME_CANON_ENTRIES = ${JSON.stringify(entries, null, 1)};
`;
writeFileSync(outCanon, canonSrc);
console.log(`WROTE ${outCanon} (${entries.length} entries)`);
